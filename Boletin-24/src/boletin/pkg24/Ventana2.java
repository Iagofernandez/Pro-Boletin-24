    private void DatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DatosActionPerformed
        
        if(evt.getSource()==Datos){
            if(jList1.isSelectionEmpty()==true){
                Area.setText(campoNome.getText() + " sin ciclo seleccionado");
            }else{
                Area.setText(campoNome.getText() + " Ciclo " 
                        +jList1.getSelectedValue());
            }
        }
    }//GEN-LAST:event_DatosActionPerformed

    



